"""Generate the relyloop.com Guides pages from the in-repo source assets.

Single source of truth:
  - ``ui/public/guides/<NN_slug>/`` — 10 walkthrough decks (metadata.json +
    numbered PNGs + walkthrough.webm [+ walkthrough.mp4 when transcoded]).
  - ``docs/08_guides/*.md`` — 4 long-form guides.

This generator mirrors those into the MkDocs Material site under
``website/docs/``, emitting:
  - ``website/docs/guides/walkthroughs/<slug>.md`` (one per deck) + index.md
  - ``website/docs/guides/in-depth/<basename>`` (one per long-form) + index.md
  - ``website/docs/assets/guides/<slug>/*`` (copied PNG + WebM [+ MP4])
  - a managed ``Guides:`` nav fragment inside ``website/mkdocs.yml`` (between
    BEGIN/END sentinels — the rest of the nav stays hand-written).

It is STDLIB-ONLY (no PyYAML, no third-party imports) so it runs on the
Python-only GitHub-Pages deploy runner and at PR time in the freshness gate
without any extra ``pip install``. Mirrors the single-direction-mirror +
prune discipline of ``ui/scripts/copy-docs.mjs``.

Run:
    python website/scripts/build_guides.py              # generate (default)
    python website/scripts/build_guides.py --transcode  # also (re)produce MP4s via ffmpeg

Importing this module does NOT trigger generation — the bottom-of-file
``if __name__ == "__main__":`` entrypoint gates the run, so the pytest at
``backend/tests/unit/scripts/test_build_guides.py`` can import and exercise
the individual functions against tmp fixtures.

The generated ``.md`` files and the three destination directories
(``website/docs/guides/walkthroughs/``, ``website/docs/guides/in-depth/``,
``website/docs/assets/guides/``) are GENERATOR-OWNED — do not hand-edit;
re-running prunes anything the generator didn't emit.
"""

