'use client';

/**
 * Generic debounce hook (feat_data_table_primitive Story 2.4).
 *
 * Returns `value` after it has remained unchanged for `delayMs`. The first
 * value emitted on mount is the initial value; subsequent updates re-arm
 * the timer.
 */

import { useEffect, useState } from 'react';

export function useDebouncedValue<T>(value: T, delayMs: number): T {
  const [debounced, setDebounced] = useState(value);
  useEffect(() => {
    const handle = setTimeout(() => setDebounced(value), delayMs);
    return () => clearTimeout(handle);
  }, [value, delayMs]);
  return debounced;
}
